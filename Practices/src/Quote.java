/**
 * This class prints two quotes onto the screen and illustrates the use of Escape Sequences
 *
 * @author Asaad
 */
public class Quote
{
    public static void main(String[] args)
    {
        // A quote by Winston Churchill
        System.out.print("\"Success is stumbling from failure to failure");
        System.out.print(" with no loss of enthusiasm\"\n");
        System.out.print("\t\t\t\t\t\t- Winston Churchill\n\n");

        // A quote by Thomas Edison
        System.out.print("\"I have not failed. I've just found 10,000 ways that won't work\"\n");
        System.out.print("\t\t\t\t\t\t- Thomas Edison\n");
    }
}

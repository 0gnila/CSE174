/*  With this program we need to make a Cat Emoji with the print statement */
public class EmojiPattern
{
    public static void main(String[] args)
    {
        //This is the Cat's Ears
        System.out.println("  /\\_/\\");
        //This is the Cat's Eyes and Nose
        System.out.println(" ( o.o )");
        //This is the Cat's Arms and his snack
        System.out.println("  > ^ <");
        //This is the Cat's lower body
        System.out.println(" / // \\ \\");
        //This is the Cat's Bed
        System.out.println("\\________/");
    }

}
